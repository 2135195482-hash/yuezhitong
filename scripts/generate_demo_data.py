def score_from_rank(rank):
    """从排名粗略估算分数（仅用于演示）"""
    if rank <= 100: return 700
    if rank <= 500: return 680
    if rank <= 1000: return 670
    if rank <= 3000: return 660
    if rank <= 5000: return 650
    if rank <= 8000: return 640
    if rank <= 12000: return 630
    if rank <= 16000: return 620
    if rank <= 20000: return 610
    if rank <= 25000: return 600
    if rank <= 30000: return 590
    if rank <= 35000: return 580
    if rank <= 40000: return 570
    if rank <= 48000: return 560
    if rank <= 55000: return 550
    if rank <= 65000: return 540
    if rank <= 75000: return 530
    if rank <= 85000: return 520
    if rank <= 95000: return 510
